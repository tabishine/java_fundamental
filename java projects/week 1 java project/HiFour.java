public class HiFour {

    public static void main(String[] args) {
        // takes 4 command-line arguments and prints them in reverse order
        System.out.print("Hi ");
        System.out.print(args[3]);
        System.out.print(", ");
        System.out.print(args[2]);
        System.out.print(", ");
        System.out.print(args[1]);
        System.out.print(", and ");
        System.out.print(args[0]);
        System.out.println(".");

    }
}
